new = ["@", "8", "(", "|)", "3", "#", "6", "[-]", "|", "_|", "|<", "1", "[]\/[]",
       "[]\[]", "0", "|D", "(,)", "|Z", "$", "']['", "|_|", "\/", "\/\/", "}{", "`/", "2", ]

a = input()
s = ""
for i in range(len(a)):
    if a[i].isalpha():
        s += new[ord(a[i].lower()) - 97]
    else:
        s += a[i]
print(s)
